package com.smallday;


public class Employee {

    private String name;
    private String exit;
    private String entry;
    private String diff;



    public void setName( String newName) {
        name = newName;
    }

    public void setEntry(String newEntry) {
        entry = newEntry;
    }

    public void setExit( String newExit) {
        exit = newExit;
    }
    public void setDiff( String newDiff) {
        diff = newDiff;
    }
}




